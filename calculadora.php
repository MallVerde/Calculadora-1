<?php
  function calculadora($x, $y, $operacion){
    if($operacion == "+")
        return $x + $y;
    else if($operacion == "+")
        return $x + $y;
    else if($operacion == "-")
        return $x - $y;
    else if($operacion == "*")
        return $x * $y;
    else if($operacion == "/")
        return $x / $y;
    return 0;
  }
?>